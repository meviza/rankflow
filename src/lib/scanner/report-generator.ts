    geo: { tr: "GEO", en: "GEO", de: "GEO" },
    local: { tr: "Lokal", en: "Local", de: "Lokal" },
    competitor: { tr: "Rekabet", en: "Competitor", de: "Wettbewerb" },
    overall: { tr: "Genel", en: "Overall", de: "Gesamt" },
    phaseUrgent: { tr: "Faz 1: Acil (1-4 Hafta)", en: "Phase 1: Urgent (1–4 Weeks)", de: "Phase 1: Dringend (1–4 Wochen)" },
    phaseStructural: { tr: "Faz 2: Yapısal (1–3 Ay)", en: "Phase 2: Structural (1–3 Months)", de: "Phase 2: Strukturell (1–3 Monate)" },
    phaseStrategic: { tr: "Faz 3: Stratejik (3–6 Ay)", en: "Phase 3: Strategic (3–6 Months)", de: "Phase 3: Strategisch (3–6 Monate)" },
