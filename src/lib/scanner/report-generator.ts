    noIssues: { tr: "Herhangi bir sorun bulunamadı.", en: "No issues found.", de: "Keine Probleme gefunden." },
    applied: { tr: "✓ Uygulandı", en: "✓ Applied", de: "✓ Angewendet" },
    notApplied: { tr: "○ Bekliyor", en: "○ Pending", de: "○ Ausstehend" },
    effort: { tr: "Efor (saat)", en: "Effort (hours)", de: "Aufwand (Std.)" },
